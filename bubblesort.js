function bubbleSort(arr) {
  let temp;
  for (let i = arr.length - 1; i > 0; i--) {
    for (let j = 0; j < arr.length; j++) {
      if (arr[j - 1] > arr[j]) {
        temp = arr[j - 1];
        arr[j - 1] = arr[j];
        arr[j] = temp;
      }
    }
  }
  return arr;
}
bubbleSort([1, 10, 8, 5, 7, 6, 3, 4, 2]);
